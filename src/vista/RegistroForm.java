    private void btnregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistrarActionPerformed
        // TODO add your handling code here:
        if(!txtnombre.getText().equals("")&&!txtapellidos.getText().equals("")&&!txtcarrera.getText().equals("")&&
                !txtciclo.getText().equals("")&&!txtturno.getText().equals("")&&!txtfecha.getText().equals("")&&
                !txtmodalidad.getText().equals("")){
            Alumno a = new Alumno();
            a.setNombre(txtnombre.getText());
            a.setApellidos(txtapellidos.getText());
            a.setCarrera(txtcarrera.getText());
            a.setCiclo(txtciclo.getText());
            a.setTurno(txtturno.getText());
            a.setFecha(txtfecha.getText());
            a.setModalidad(txtmodalidad.getText());
            dao.add(a);
            llenardatos();
            JOptionPane.showMessageDialog(rootPane,"Guardado Correctamente");
            limpiar();
        }else{
            JOptionPane.showMessageDialog(rootPane,"Campos Vacios");
        }
    }//GEN-LAST:event_btnregistrarActionPerformed

    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed
        // TODO add your handling code here:
        if(fila>=0){
            Alumno a = new Alumno();
            a.setNombre(txtnombre.getText());
            a.setApellidos(txtapellidos.getText());
            a.setCarrera(txtcarrera.getText());
            a.setCiclo(txtciclo.getText());
            a.setTurno(txtturno.getText());
            a.setFecha(txtfecha.getText());
            a.setModalidad(txtmodalidad.getText());
            dao.actualizar(fila,a);
            llenardatos();
        }else{
            JOptionPane.showMessageDialog(rootPane,"Seleccionar Flia");
        }
    }//GEN-LAST:event_btnmodificarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        if(fila>=0){
            dao.eliminar(fila);
            limpiar();
            llenardatos();
        }else{
            JOptionPane.showMessageDialog(rootPane,"Seleccionar Flia");
        }
    }//GEN-LAST:event_btneliminarActionPerformed

