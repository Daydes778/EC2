private void limpiar(){
    txtnombre.setText("");
    txtapellidos.setText("");
    txtcarrera.setText("");
    txtciclo.setText("");
    txtturno.setText("");
    txtfecha.setText("");
    txtmodalidad.setText("");
    txtnombre.requestFocus();
}
private void limpiartabla(){
    for(int i=0;i<tbdatos.getRowCount();i++){
        model.removeRow(i);
        i-=1;
    }
        
}
public void llenardatos(){
    limpiartabla();
    List<Alumno> lista= new ArrayList<>();
    model = (DefaultTableModel)tbdatos.getModel();
    lista = dao.getLista();
    Object[] fila = new Object[8];
    for(int j=0;j<lista.size();j++){
        fila[0] = j+1;
        fila[1] = lista.get(j).getNombre();
        fila[2] = lista.get(j).getApellidos();
        fila[3] = lista.get(j).getCarrera();
        fila[4] = lista.get(j).getCiclo();
        fila[5] = lista.get(j).getTurno();
        fila[6] = lista.get(j).getFecha();
        fila[7] = lista.get(j).getModalidad();
        model.addRow(fila);
    }
    tbdatos.setModel(model);
}
